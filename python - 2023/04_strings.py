name = "Edward"
last_name = 'Snowden'
full_name = name + " " + last_name

print(full_name)

# Format
print("Hello, my name is {} {}".format(name, last_name))

# F-String
print(f"Hello, my name is {name} {last_name}")

# String Methods
print(full_name.upper())
print(full_name.lower())
print(full_name.title())
print(full_name.capitalize())

