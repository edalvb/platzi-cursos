# String
print("String")
my_string = "Hello World"
print(my_string)
print(type(my_string), "\n")

# Integer
print("Integer")
my_int = 10
print(my_int)
print(type(my_int), "\n")

# Float
print("Float")
my_float = 10.5
print(my_float)
print(type(my_float), "\n")

# Boolean
print("Boolean")
my_bool = True
print(my_bool)
print(type(my_bool), "\n")

# List
print("List")
my_list = [1, 2, 3, 4, 5]
print(my_list)
print(type(my_list), "\n")

# Tuple
print("Tuple")
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)
print(type(my_tuple), "\n")

# Dictionary
print("Dictionary")
my_dict = {"name": "John", "age": 30}
print(my_dict)
print(type(my_dict), "\n")

# Set
print("Set")
my_set = {1, 2, 3, 4, 5}
print(my_set)
print(type(my_set), "\n")

# Frozen Set
print("Frozen Set")
my_frozenset = frozenset([1, 2, 3, 4, 5])
print(my_frozenset)
print(type(my_frozenset), "\n")

# Range
print("Range")
my_range = range(1, 10)
print(my_range)
print(type(my_range), "\n")

# Complex
print("Complex")
my_complex = 1j
print(my_complex)
print(type(my_complex), "\n")

# Bytes
print("Bytes")
my_bytes = b"Hello"
print(my_bytes)
print(type(my_bytes), "\n")

# Byte Array
print("Byte Array")
my_bytearray = bytearray(5)
print(my_bytearray)
print(type(my_bytearray), "\n")

# Memory View
print("Memory View")
my_memoryview = memoryview(bytes(5))
print(my_memoryview)
print(type(my_memoryview), "\n")

# None
print("None")
my_none = None
print(my_none)
print(type(my_none), "\n")
